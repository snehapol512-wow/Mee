package Association.aggregation;

public class Information {
    char blockNo;
    int floorNo;
    int bedNo;
    int roomNo;

    public Information(char blockNo, int floorNo, int bedNo, int roomNo) {
        this.blockNo = blockNo;
        this.floorNo = floorNo;
        this.bedNo = bedNo;
        this.roomNo = roomNo;
    }
}
